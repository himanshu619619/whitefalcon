												<p>The day is reserved for a full-day tour of Udaipur. After the breakfast, visit the various tourist spots of Udaipur like Jagdish Temple, Sahelion-ki-bari and the City Palace. Enjoy a boat ride on Lake Pichola in the evening. Return to the hotel for the night</p>
