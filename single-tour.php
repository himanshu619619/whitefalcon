											<div class="item_content">
												<h2><strong>Day 8: Khajuraho  </strong></h2>
												<p>After the breakfast, take a road-trip back to Khajuraho. On arrival, check-in at the hotel. Rest of the day is for leisure activities.</p>
											</div>
										</div>
										<div class="interary-item">
											<p><span class="icon-left">9</span></p>
											<div class="item_content">
												<h2><strong>Day 9: Agra  </strong></h2>
												<p>After breakfast, drive to Orchha to visit the famous Ram Raja Temple. Have your lunch at Orchha. Afterwards, drive to Jhansi and proceed to the railway station. Board a train from the railway station to Agra, Uttar Pradesh. On arrival, transfer to the hotel for check-in and spending the night.</p>
											</div>
										</div>
										<div class="interary-item">
											<p><span class="icon-left">10</span></p>
											<div class="item_content">
												<h2><strong>Day 10: Agra  </strong></h2>
												<p>The day after the breakfast is reserved for visiting the most famous monument in India, Taj Mahal. The city of Agra was once the imperial capital of India under the Mughal rule. Besides Taj, you can also visit the tomb of Itamud-ud-daula and Agra Fort. Evening time is free for strolling around the local markets. Get back to the hotel for the night.</p>
											</div>
										</div>
