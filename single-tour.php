												<h2><strong>Day 4 Varanasi </strong></h2>
												<p>Enjoy a leisurely breakfast in the hotel. Then, transfer to the railway station to board a train to Varanasi. On arrival, proceed to the hotel for checking in. Spend the night at the hotel</p>
											</div>
