												<p>Lucknow is the capital of Uttar Pradesh. It has many buildings belonging to the eighteenth century like the Imambaras. You can also visit the Dilkusha gardens and the Residency Complex. Return to the hotel for spending the night.</p>
											</div>
										</div>
