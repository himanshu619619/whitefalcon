											<div class="item_content">
												<h2><strong>Day 11: Jaipur  </strong></h2>
												<p>After breakfast, proceed to Fatehpur Sikri. It is an old, but well-preserved ghost town created by Mughal Emperor Akbar. After lunch, proceed for Jaipur in Rajasthan by road. Arrive in Jaipur and proceed to the hotel for checking-in and overnight stay.</p>
											</div>
										</div>
										<div class="interary-item">
											<p><span class="icon-left">12</span></p>
											<div class="item_content">
												<h2><strong>Day 12: Jaipur  </strong></h2>
												<p>Jaipur is the capital of Rajasthan and is one of the most vibrant cities of India in terms of culture. Take a day tour of the city after the breakfast. You can visit the Amber Fort, Sawai Man Singh Museum and Hawa Mahal. After lunch, visit the City Palace and the local markets. Evening time is free time. Spend the night back at the hotel.</p>
											</div>
										</div>
										<div class="interary-item">
