												<p>On the second day after the breakfast, take a half-day tour of Delhi. You can visit the classical beauties of Delhi like the seat of the former Mughal Empire, Red Fort; the busy markets of Chandni Chowk: tomb of Humayun and Jama Masjid. After the lunch, drive to the airport to board a flight to Lucknow. On arrival, you will be transferred to the hotel where you will be spending the night.</p>
