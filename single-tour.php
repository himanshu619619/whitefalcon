												<h2><strong>Day 13: Deogarh  </strong></h2>
												<p>Drive to Deogarh in the morning after the breakfast to see the Deogarh Mahal. Afterwards, check-in at the hotel and spend the rest of the day as you like</p>
											</div>
										</div>
