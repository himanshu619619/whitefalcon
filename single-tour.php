												<h2><strong>Day 14: Udaipur  </strong></h2>
												<p>After breakfast, proceed to Udaipur by road. Udaipur is another royal city of Rajasthan. On arrival, transfer to the hotel for check-in and relaxing. In the afternoon, visit the famous Ranakpur temples. Overnight stay at Hotel. </p>
											</div>
										</div>
