											<p><span class="icon-left">5</span></p>
											<div class="item_content">
												<h2><strong>Day 5 Khajuraho       </strong></h2>
												<p>On the fifth day, get up early in the morning to see the fascinating rituals performed at the ghats (cemented or stone steps near a riverbank). Return to the hotel for breakfast. Afterwards, take a day tour of the Varanasi that includes sightseeing at the Vishwanath Temple, Kaal Bhairav Temple and Gauri Matha Temple. Now proceed to the airport to board a flight to Khajuraho, Madhya Pradesh. On arrival, transfer to the hotel for checking in. After lunch, visit the famous temples of Khajuraho, which are marked as a world heritage site by UNESCO. Return to the hotel for spending the night.</p>
											</div>
										</div>
										<div class="interary-item">
											<p><span class="icon-left">6</span></p>
											<div class="item_content">
												<h2><strong>Day 6: Bandhavgarh National Park </strong></h2>
												<p>After a relaxing breakfast, visit the remaining temples and monuments of Khajuraho. Then, take a road-trip to the Bandhavgarh National Park. On arrival, check into a jungle cottage. Spend the rest of the evening at the cottage.</p>
											</div>
										</div>


										<div class="interary-item">
											<p><span class="icon-left">7</span></p>
											<div class="item_content">
												<h2><strong>Day 7: Bandhavgarh National Park </strong></h2>
												<p>On the seventh day after the breakfast at the cottage, take jungle safari to see the Royal Bengal Tiger and other rare animals. You can choose either an elephant safari or jeep safari four your jungle tour. Return to the cottage for spending the night.</p>
											</div>
										</div>


										<div class="interary-item">
